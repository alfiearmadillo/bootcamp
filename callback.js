function resolveAfterSeconds(time) {
    return new Promise(resolve => {
        setTimeout(() => {
            resolve(time);
        }, 1000);
    });
}

async function countdown(time) {
    for (i = 0; i < time+1; i++) {
        process.stdout.clearLine(await resolveAfterSeconds(0));
        process.stdout.cursorTo(0);
        process.stdout.write("" + (time) - i + "");
    };
    console.log("");
    console.log("Finished my",subject,"homework in", time, "seconds.");
}

