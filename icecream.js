let iceCream = {
    toppings:[],
    flavour:null,
    coneType:null,
    price:0
};

let cones = ["waffle", "sugar", "cake"];
let flavours = ["vanilla", "strawberry", "raspberry", "chocolate", "mint", "banana", "pistachio", "cherry", "among_ice"];
let toppings = ["chocolate_chips", "almond", "sprinkles", "chocolate_sauce", "strawberry_sauce"];
let tempValue = null;

async function kitchen() {

    iceCream.coneType = cones[await pickCone()];

    iceCream.flavour = flavours[await pickFlavour()];

    for(i=0;i<Math.floor(Math.random() * 3);i++){
        tempValue = await pickToppings()
        iceCream.toppings[i]=toppings[tempValue];
        toppings.splice(tempValue, 1);
    };

    makeIceCream();

}

function pickCone() {
    return new Promise(resolve => {
        setTimeout(() => {
            let tempRandom = Math.floor(Math.random() * cones.length)
            console.log(`I think I'll have a ${cones[tempRandom]} cone,`)
            iceCream.price=iceCream.price+tempRandom;
            resolve(tempRandom);
        }, 2000);
    });
}

function pickFlavour() {
    return new Promise(resolve => {
        setTimeout(() => {
            let tempRandom = Math.floor(Math.random() * flavours.length)
            console.log(`with some ${flavours[tempRandom]} ice cream`)
            iceCream.price=iceCream.price+tempRandom;
            resolve(tempRandom)
        }, 2000);
    });
}

function pickToppings() {
    return new Promise(resolve => {
        setTimeout(() => {
            let tempRandom = Math.floor(Math.random() * toppings.length)
            console.log(`and maybe some ${toppings[tempRandom]}...`)
            iceCream.price=iceCream.price+tempRandom;
            resolve(tempRandom)
        }, 2000);
    });
}

